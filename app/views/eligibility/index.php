

    <h2>Undergraduate Program:</h2>
    1. Minimum GPA of 2.50 in both SSC and HSC Examinations No third division is allowed. or
    <br/> <br/>
    2. Candidates must have passed University of London and Cambridge GCE 'O' Level in at least five subjects and 'A' Level in at least two subjects.  Only the best five subjects in 'O' Level and best two subjects in 'A' Level will be considered. Out of these seven subjects, a candidate must have at least 4B's or GPA of 4.00 in the four subjects and 3 C's or GPA of 3.5 in the remaining three subjects(in the scale of A=5, B=4, C=3, D=2 and E=1) or
    <br/> <br/>
    3. American High School Diploma, AND
    <br/> <br/>
    4. Acceptable EWU Admission Test Score.
    <br/> <br/>
    5. Total GPA of 5.00 in both SSC and HSC Examinations for the  children of Freedom Fighter.
    <br/> <br/>
    6. The final selection of candidates for admission in the Undergraduate Programs at EWU will be based on the Admission Test scores obtained with 75% from admission test, 10% from SSC/O-level and 15% from HSC/A-level.

    <h2> Admission test waiver:</h2>
    The candidates securing a minimum total score of 1500 in SAT (considering Critical Reading, Math and Writing). The candidates seeking admission in the Faculty of  Sciences and Engineering must have minimum Grade Point 3.5 in Math and Physics separately in HSC/A level examinations in addition to above mentioned SAT score to get waiver from the Admission Test.

    <h2> For foreign students:</h2>
    Foreign Students particularly who come from other systems like US High School Diploma, Indian/Nepalese system etc. (not from SSC/HSC or O/A Level etc. system) the admission eligibility will be as follows: <br/> <br/>
    1.	Must have 12 years of schooling.
    <br/><br/>
    2.	SAT score of 1100 or
    <br/><br/>
    3.	Upper 50% marks/grade of their own education system <br/> <br/>
    An equivalence committee will assess and recommend for satisfactory grade for the applicants who seek admission in EWU with US High School Diploma or who come from other systems. A committee will assess and recommend for Scholarship/Financial Aid etc. (if applicable) for foreign students and the students from other systems.
